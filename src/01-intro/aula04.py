# Aula 04 - Var & Const $ Literais

# Inferencia de tipo
num = 10
print(num)
print(type(num))

num = 20
print(num)

# Multiplas atribuições
nome, idade, ano = 8, 'Maria', 2000
print(nome, idade, ano)

# snake_case
id_num = 324

# Constantes --> SEMPRE EM UPERCASE
nome = 'Joao'
print(nome)

# Literais
idade = 23
print(23)  # Literal
print(idade)

"""Boolean"""
print(True, type(True))
print(False, type(False))

"""None"""
print(None, type(None))

"""Coleçoes"""
# Listas
numeros = [1, 2, 3]
print(numeros, type(numeros))

# Tupla
senhas = ('coxinha123', 'bruno321')
print(senhas, type(senhas))

# Conjunto (set)
nomes = {'joao', 'maria', 'joao'}
print(nomes, type(nome))

# Dicionario
aluno = {'prontuario': 1213,
         'nome': 'Joao',
         'idade': 12}

print(aluno, type(aluno))
